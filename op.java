class op
{
static int add(int q,int w)
{
return q+w;
}
static int sub(int q,int w)
{
return q-w;
}
static int multi(int q,int w)
{
return q*w;
}
static int div(int q,int w)
{
return q/w;
}
public static void main(String args[])
{
System.out.println(add(5097,3765));
System.out.println(sub(3858,2984));
System.out.println(multi(14,95));
System.out.println(div(90782,3684));
}
}